/**
 * Takes a `name` and returns a greeting message.
 *
 * @category String
 * @example
 * ```typescript
 * console.log(greeting("Vangware")); // Hello, Vangware!
 * ```
 * @param name Name to be greeted.
 * @returns A greeting message.
 */
export const greet = (name: string) => `Hello, ${name}!`;
